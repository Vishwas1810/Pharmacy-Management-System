    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String uid = txtUID.getText();
        String upass = txtUPass.getText();            
        int flag = 0;
        
        try {
            pst = con.prepareStatement("select UserId, UserName, Password, LoginAs from Login_Details");
            rs = pst.executeQuery();
            
            while(rs.next()) {
                String prev = rs.getString("LoginAs");
                if(uid.equals(rs.getString("UserId")) && upass.equals(rs.getString("Password"))) {
                    if(prev.equals("U")) {
                        String name = rs.getString("UserName");
                        JOptionPane.showMessageDialog(null, "Login Successful\n"+name+" Logged As User");
                        flag = 1;
                        User u = new User();
                        this.hide();
                        u.UserInfo(name);
                        u.setVisible(true);
                        break;
                    }
                    else if(prev.equals("A")) {
                        String name = rs.getString("UserName");
                        JOptionPane.showMessageDialog(null, "Login Successful\nWelcome Admin : " +name);
                        flag = 1;
                        Admin a = new Admin();
                        this.hide();
                        a.LoginAs(name);
                        a.setVisible(true);
                        
                        break;
                    }
                    else if(prev.equals("E")) {
                        String name = rs.getString("UserName");
                        JOptionPane.showMessageDialog(null, "Login Successful\n"+name+" Logged As Employee");
                        flag = 1;
                        Employee e = new Employee();
                        this.hide();
                        e.LoginInfo(name, rs.getString("UserId"));
                        e.setVisible(true);
                        break;
                    }
                }                
            }
            
            if(flag == 0){
                JOptionPane.showMessageDialog(null, "Either UserName or Password Error", "Error", JOptionPane.ERROR_MESSAGE);
                txtUID.setText("");
                txtUPass.setText("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

